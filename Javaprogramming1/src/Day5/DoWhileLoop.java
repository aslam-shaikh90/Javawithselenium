package Day5;

public class DoWhileLoop {

	public static void main(String[] args) {
		
		
		// Example 1 printing 1 .... 10 number
		
		int i=1;
		
		do
		{
			System.out.println(i);
			i++;
		}while(i<=10);
		
		
		// Example 2 printing 10 .... 1 number
		
				int a=10;
				
				do
				{
					System.out.println(a);
					a--;
				}while(a>=10);
	}
	
}
