package Day2;

public class VariablesDemo {
	
	public static void main(String[] args) 
	{
		// Approach 1 - If the all variables belong to different Data type 
		int a;
		int b;
		int c;
		a=100;
		b=200;
		c=300;
		System.out.println("This is value of : "+a);
		System.out.println("This is value of : "+b);
		System.out.println("This is value of : "+c);
		
		System.out.println(a-b);
		
		
		
		// Approach 2  -- If the all variables belong to same Data type
		
		int d,e,f;
		d=100;e=200;f=300;
		
		System.out.println("This is a value of :"+d);
		System.out.println("This is a value of :"+e);
		System.out.println("This is a value of :"+f);
		System.out.println("This is a Sum of a,b,c:"  +(d+e+f));
		
		
		
		// Approach 3  -- If the all variables belong to same Data type
		
		int x=100,y=200,z=300;
		
		System.out.println(x+" "+y+" "+z);
		
		
	}

}


 
